package Primitives;

public class Primitives {

	public static void main(String[] args) {
		byte byteMaxVal = Byte.MAX_VALUE;
		short shortMaxVal = Short.MAX_VALUE;
		int intMaxVal = Integer.MAX_VALUE;
		long longMaxVal = Long.MAX_VALUE;
		float floatMaxVal = Float.MAX_VALUE;
		double doubleMaxVal = Double.MAX_VALUE;
		boolean booleanVal = Boolean.FALSE;
		char charVal = Character.MAX_VALUE;
		
		System.out.println("byteMaxVal : " + byteMaxVal);
		System.out.println("shortMaxVal : " + shortMaxVal);
		System.out.println("intMaxVal : " + intMaxVal);
		System.out.println("longMaxVal : " + longMaxVal);
		System.out.println("floatMaxVal : " + floatMaxVal);
		System.out.println("doubleMaxVal : " + doubleMaxVal);
		System.out.println("booleanVal : " + booleanVal);
		System.out.println("charVal : " + charVal);
	}

}
